async function testSupabaseIntegration() {
  console.log('--- Testing API Option B (Headless CMS + JSON fields) ---');
  
  const API_URL = 'http://localhost:3000/api/save-article';
  
  const payload = {
    title: "Test Article Option B " + Date.now(),
    excerpt: "This is a test article generated by the new Cerebro JSON integration.",
    category: "automatizacion",
    image: "/images/placeholder.jpg",
    metaDescription: "Test article SEO description goes here.",
    markdown: `
## Welcome to Option B

This markdown content should be saved directly in the Supabase \`content\` field, while the rest goes to their respective JSON columns.
    `,
    originalSlug: ""
  };

  try {
    const res = await fetch(API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json'
      },
      body: JSON.stringify(payload)
    });

    const data = await res.json();
    console.log('Response Status:', res.status);
    console.log('Response Data:', JSON.stringify(data, null, 2));
    
  } catch (error) {
    console.error('Test Failed:', error);
  }
}

testSupabaseIntegration();
